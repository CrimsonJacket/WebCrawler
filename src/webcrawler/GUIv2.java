    public class ProgressBarRender extends JProgressBar implements TableCellRenderer{

        @Override
        public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
            int progress = 0;
            if (value instanceof Float){
                progress = Math.round((float)value /100);
                setValue(progress);
            }else if (value instanceof Integer){
                progress = (int)value;
                setValue(progress);
            }else if (value instanceof  String){
                setString((String)value);       
            }
            setStringPainted(true);
            return this;
        }
    }
    
    public class UrlHanderWorker extends SwingWorker<Object, Object>{
        private DefaultTableModel dtm;
        private String url;
        private int row;
        
        public UrlHanderWorker(DefaultTableModel dtm, String url, int row){
            this.dtm = dtm;
            this.url = url;
            this.row = row;
        }
        
        @Override
        protected Object doInBackground() throws Exception {
            int timer = 0;
            dtm.setValueAt("Downloading", row, 2);
            while (timer != 105){
                dtm.setValueAt(timer, row, 1);
                Thread.sleep(1000);
                timer +=5;
                if (timer == 50) dtm.setValueAt("Processing", row, 2);
            }
            dtm.setValueAt("Processed", row, 2);
            dtm.setValueAt(0, row, 4);
            return this;
        }
        
    }
